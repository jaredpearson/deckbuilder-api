CREATE TABLE Cards (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY,
	name VARCHAR(50),
	powerToughness VARCHAR(10),
	castingCost VARCHAR(20),
	text VARCHAR(500),
	typeLine VARCHAR(65),
	picUrl VARCHAR(100),
	cardSet INTEGER NOT NULL REFERENCES Sets(id),
	setIndex VARCHAR(4),
	rarity CHAR(1),
	artist VARCHAR(50)
)